# pylint: disable = invalid-name
""" Módulo math """
import math

print(round(1.3))
print(round(1.7))
print(round(1.5))

# Devuelve el valor absoluto de un número. Siempre será positivo
print(abs(-11))

# Devuelve el número entero superior más cercano
print(math.ceil(1.1))

# Devuelve el número entero inferior más cercano
print(math.floor(1.1))

# Devuelve un boolean dependiendo de si el valor especificado es un número real. False si es un número real.
print(math.isnan(1))
# print(math.isnan("1"))

# pow siempre devuelve un float
print(math.pow(10, 3))

# sqrt devuelve la raíz cuadrada de un número
print(math.sqrt(9))
