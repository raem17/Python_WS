""" Secuencias de escape """

nombre = 'Alejandro "el humano"'

# Se usa \ para indicar a Python que interprete el carácter de la derecha como un string
nombre2 = "Alejandro \"el humano\""
cadena = "hola mundo \\"

# Se usa \n para imprimir un salto de línea
cadena2 = "Hola\nMundo"

print(nombre)
print(nombre2)
print(cadena)
print(cadena2)
