frutas = ["Manzana", "Pera", "Nectarina", "Naranja"]

# Imprimir un elemento de la lista
print(frutas[0])

# Cambiar el contenido de una posición de una lista
frutas[0] = "Sandía"
print(frutas[0])

# Imprimir un rango de elementos de "frutas" hasta la posición 3 (no incluida)
print(frutas[:3])

# Imprimir un rango de elementos de "frutas" desde la posición 2 (incluida)
print(frutas[2:])

# Imprimir el elemento de la última posición de "frutas"
print(frutas[-1])
print()

# Imprime un elemento y salta el siguiente...
print(frutas[::2])

# Imprime un elemento y salta los dos siguientes...
print(frutas[::3])
